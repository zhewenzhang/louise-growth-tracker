// 計算某個數值在 WHO 分布中的百分位
export const calculatePercentile = (value, type, monthAge) => {
  if (monthAge < 0 || monthAge > 24) return null;

  const dataMap = {
    weight: whoWeightData,
    height: whoHeightData,
    headCircumference: whoHeadCircumferenceData
  };

  const data = dataMap[type];
  if (!data) return null;

  const index = Math.min(Math.floor(monthAge), 23);
  const p3 = data.p3[index];
  const p50 = data.p50[index];
  const p97 = data.p97[index];

  if (value < p3) return 'Below P3';
  if (value < p50) return `P3-P50 (${Math.round((value - p3) / (p50 - p3) * 47) + 3}th)`;
  if (value < p97) return `P50-P97 (${Math.round((value - p50) / (p97 - p50) * 47) + 50}th)`;
  return 'Above P97';
};
